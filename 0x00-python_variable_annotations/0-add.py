#!/usr/bin/env python3
'''A Python Script that implements type annotation'''


def add(a: float, b: float) -> float:
    '''
        add: A function that take in float and returns a float
        Args:
            a: A floating point number
            b: A floating point number
        Returns:
            A new sum of both a and b also a floating point number
    '''
    return a + b
