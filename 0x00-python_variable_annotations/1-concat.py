#!/usr/bin/env python3
'''A Python script that returns the concatination of two strings'''


def concat(str1: str, str2: str) -> str:
    '''A function for concatinating two strings as one'''
    '''
        Args:
            str1: A string to be conatinated
            str2: A string to be concatinated
        Returns:
            A new string containing both str1 and str2
    '''
    return str1 + str2
